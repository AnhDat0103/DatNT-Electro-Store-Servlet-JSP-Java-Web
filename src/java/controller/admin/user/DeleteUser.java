    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String id = request.getParameter("ma-khach-hang");

        User u = ud.findUserById(validation.Validate.getInteger(id));
        if (u == null) {
            request.setAttribute("error", "Người dùng không tông tại.");
        } else {
            request.setAttribute("u", u);
        }
        request.getRequestDispatcher("/dashboard/user/delete.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String userIdRequest = request.getParameter("user_id");
        if(!userIdRequest.isEmpty()) {
            int rowEffectd = ud.delete(Integer.parseInt(userIdRequest));
            if(rowEffectd > 0) {
                response.sendRedirect("/electro-store/admin/quan-ly-khach-hang");
            }
        }
        
    }

